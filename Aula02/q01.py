numero01 = int(input("Escolha um número: "))
numero02 = int(input("Escolha outro número:"))

if numero01 > numero02:
    print(f"O {numero01} é maior que o {numero02}")
elif numero01 < numero02: 
    print(f"O {numero01} é menor que o {numero02}")
elif numero01 == numero02:
    print("Os números são iguais")
    
